# Addition (+)
# Substration (-)
# Multiplication (*)
# Division (/)
# Modulus (%) //remainder after dividing
# Exponent (**)

x = 10
y = 2

print(x + y) # result: 12

print(x - y) # result: 8

print(x * y) # result: 20
print(x / y) # result: 5.0
print(x % y) # result: 0
print(5 % 2) # result: 1
print(x ** y) # result: 100

x = x + 2
print(x) # result: 12
x += 2
print(x) # result: 14
x = x - 2
print(x) # result: 12
x -= 2
print(x) # result: 10